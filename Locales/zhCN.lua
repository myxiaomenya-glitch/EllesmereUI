local function GetTranslation(str)
    if not str or type(str) ~= "string" or str == "" then return nil end
    return translations[str]
end

local function MyL(str)
    return GetTranslation(str) or str
end

local function MyLf(str, ...)
    local t = MyL(str)
    if type(t) ~= "string" or select("#", ...) == 0 then return t end
    local ok, result = pcall(string.format, t, ...)
    return ok and result or t
end

local function HookWidgetMethods()
    local fs = CreateFrame("Frame"):CreateFontString()
    local fsMeta = getmetatable(fs).__index
    
    local inSetText = false
    hooksecurefunc(fsMeta, "SetText", function(self, text)
        if inSetText then return end
        
        local parent = self:GetParent()
        if parent and parent.IsProtected and parent:IsProtected() then return end
        
        local success, translated = pcall(function()
            if type(text) == "string" and text ~= "" then
                local trans = GetTranslation(text)
                if trans and trans ~= text then
                    return trans
                end
            end
            return nil
        end)
        
        if success and translated then
            inSetText = true
            pcall(self.SetText, self, translated)
            inSetText = false
        end
    end)

    local inSetFormattedText = false
    hooksecurefunc(fsMeta, "SetFormattedText", function(self, fmt, ...)
        if inSetFormattedText then return end
        
        local parent = self:GetParent()
        if parent and parent.IsProtected and parent:IsProtected() then return end
        
        local success, translated = pcall(function()
            if type(fmt) == "string" then
                local trans = GetTranslation(fmt)
                if trans and trans ~= fmt then
                    return trans
                end
            end
            return nil
        end)
        
        if success and translated then
            inSetFormattedText = true
            local ok, result = pcall(string.format, translated, ...)
            if ok then
                pcall(self.SetText, self, result)
            end
            inSetFormattedText = false
        end
    end)

    local eb = CreateFrame("EditBox")
    eb:Hide() 
    local ebMeta = getmetatable(eb).__index
    
    if ebMeta.SetPlaceholderText then
        local inSetPlaceholderText = false
        hooksecurefunc(ebMeta, "SetPlaceholderText", function(self, text)
            if inSetPlaceholderText then return end
            
            local parent = self:GetParent()
            if parent and parent.IsProtected and parent:IsProtected() then return end
            
            local success, translated = pcall(function()
                if type(text) == "string" and text ~= "" then
                    local trans = GetTranslation(text)
                    if trans and trans ~= text then
                        return trans
                    end
                end
                return nil
            end)
            
            if success and translated then
                inSetPlaceholderText = true
                pcall(self.SetPlaceholderText, self, translated)
                inSetPlaceholderText = false
            end
        end)
    end
end


HookWidgetMethods()
local function PatchDataTable(EUI)
    if not EUI then return end
    local changed = false
    
    if EUI.ADDON_GROUPS then
        for _, group in ipairs(EUI.ADDON_GROUPS) do
            local translated = GetTranslation(group.label)
            if translated and translated ~= group.label then
                group.label = translated
                changed = true
            end
        end
    end

    if EUI.ADDON_ROSTER then
        for _, info in ipairs(EUI.ADDON_ROSTER) do
            local translated = GetTranslation(info.display)
            if translated and translated ~= info.display then
                info.display = translated
                changed = true
            end
        end
    end

    if changed then
        if EUI.RefreshSidebar then pcall(EUI.RefreshSidebar, EUI)
        elseif EUI.RebuildSidebar then pcall(EUI.RebuildSidebar, EUI)
        elseif EUI._sidebar and EUI._sidebar.Rebuild then pcall(EUI._sidebar.Rebuild, EUI._sidebar) end
    end
end

local function OnAddonLoaded(event, addon)
    if addon ~= "EllesmereUI_zhCN" then return end
    local EUI = _G.EllesmereUI
    if not EUI then return end
    
    EUI.L = MyL
    EUI.Lf = MyLf
    PatchDataTable(EUI)
end

local function OnPlayerReady()
    local EUI = _G.EllesmereUI
    if EUI then
        EUI.L = MyL
        EUI.Lf = MyLf
        PatchDataTable(EUI)
    end
end

local frame = CreateFrame("Frame")
frame:RegisterEvent("ADDON_LOADED")
frame:SetScript("OnEvent", OnAddonLoaded)

local loginFrame = CreateFrame("Frame")
loginFrame:RegisterEvent("PLAYER_LOGIN")
loginFrame:SetScript("OnEvent", OnPlayerReady)